var a = 0;
document.getElementById('click-element').onclick = function() {
	var block = document.getElementById('click');
	
	if (a == 0) {
		block.style.backgroundColor = "rgba(0,0,0,0)";
		a = 1;	
	} else if (a == 1) {
		block.style.backgroundColor = "#0ff";	
		a = 0;	
	};
};

