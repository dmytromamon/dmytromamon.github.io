document.getElementById('mousemove').onmousemove = function() {
	var block = document.getElementById('mousemove');
	
	block.style.backgroundColor = "rgba(0,0,0,0)";
};

setInterval(function(){
	var block = document.getElementById('mousemove');
	var block1 = document.getElementById('scroll');
	
	block.style.backgroundColor = "#0ff";	
	block1.style.backgroundColor = "#0ff";	
}, 1000);

